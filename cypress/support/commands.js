// -- This is a parent command --
Cypress.Commands.add('login', (username, password) => {
    cy.visit('http://localhost:5173/signin')
    cy.get('#username').type(username)
    cy.get('#password').type(password)
    cy.get('button').click().as('getUser')
    cy.wait(2000)
})
